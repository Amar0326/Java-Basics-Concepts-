public class Operators_ArithmeticAssignment {

  public static void main(String[] args) {
    // Operators
    int a = 6;
    int b = 2;

    // Addition
    int Sum = a + b;
    System.out.println(Sum);

    // Minus
    int Diff = a - b;
    System.out.println(Diff);

    // Multiple
    int multiple = a * b;
    System.out.println(multiple);

    // Divide
    int divide = a / b;
    System.out.println(divide);

    //Modulo
    int modulo = a % b;
    System.out.println(modulo);

    // Assignment Operator
    int Amar = 5;
    System.out.println(Amar);
    // int  -> Data Type
    // Amar -> Variable Names
    // =    -> Assignment Operator
    // 5    -> Is Value User Assign To Variable Using '='

    // Unary Operator
    int numb = 0;
    // numb++; // 1 print and then Perform Operations
    System.out.println(numb++); // Numb =2
    //  ++numb; // 1 Perform Operations and then print
    System.out.println(++numb); // Num =3
  }
}
