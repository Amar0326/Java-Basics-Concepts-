public class Casting {
    public static void main(String[] args) {
        //Casting
        // Implicit Casting-> Same Data Type To Same Data Type
        double price =100.00;
        double FinalPrice = price+18;
        System.out.println(FinalPrice);
        // Explicit Casting-> Different Data Type To Different  Data Type But We Have To Use (Data Types)
        int p=100;
        int Fp=p+((int)18.180);
        System.out.println(Fp);
    }
}
