import java.util.Arrays;

public class Array {
    public static void main(String[] args) {
// Array Initialization
int[] marks = new int[03];
        marks[0]=97;
        marks[1]=98;
        marks[2]=95;
        System.out.println(marks[0]);
// Length
        System.out.println(marks.length);
// Sort
        System.out.println(marks[0]);
        Arrays.sort(marks);
        System.out.println(marks[0]);
    }
}
